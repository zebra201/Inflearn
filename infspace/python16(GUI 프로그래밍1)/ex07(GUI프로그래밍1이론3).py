# tkinter 위젯 클래스
# Button : 간단한 버튼으로 명령을 수행
# Canvas : 화면에 무언가를 그릴때 사용
# Entry : 한 줄의 텍스트를 입력 받는 필드
# Frame : 컨테이너 클래스 (다른 위젯들을 그룹핑 하는데 사용)
# Label : 텍스트나 이미지를 표시
# Listbox : 선택사항을 표시한다
# Menu : 메뉴를 표시
# Message : 텍스트를 표시
# Scale : 슬라이더를 끌어서 수치값을 입력
# Scollbar : 캔버스, 엔트리 등을 위한 스크롤 바

# 단순 위젯과 컨테이너 위젯
