# 일반적인 더블루프 방법
for i in range(5):
    for j in range(5-i):
        print("*", end="")
    print("")
    
print("-"*50) # 다음 문제와 구분

for i in range(5,0,-1):
    for j in range(i):
        print("*", end="")
    print("")

print("-"*50) # 다음 문제와 구분

    
