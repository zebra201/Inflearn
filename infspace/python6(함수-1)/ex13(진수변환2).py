# 파이썬에서 제공하는 진법 변환 함수들
print(bin(10))  # 2진수로 변환하는 내장함수
print(oct(10))  # 8진수로 변환하는 내장함수
print(hex(10))  # 16진수로 변환하는 내장함수

print("--------------------------")
# 2진수의 값을 10진수로 변환하는 방법
print(int("0b1010", 2))

# 8진수의 값을 10진수로 변환하는 방법
print(int("0o12", 8))

# 16진수의 값을 10진수로 변환하는 방법
print(int("0xa", 16))
