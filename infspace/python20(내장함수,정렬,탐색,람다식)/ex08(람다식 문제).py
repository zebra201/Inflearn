# 2. 람다식을 이용하여 튜플리스트를 정렬하는 프로그램을 작성하라
# 원래의 리스트
# [("국어", 88),("수학",90),("영어",99),("자연",82)]
# 정렬된 리스트
# [("자연", 82),("국어",88),("수학",90),("영어",99)]

list1 = [["국어",88],
         ["수학",90],
         ["영어",99],
         ["자연",82]]
print("원래의 리스트")
print(list1)
print("점수 오름차순 리스트")
# list1.sort(key=lambda x:x[1])
# print(list1)
print(sorted(list1, key=lambda x: x[1]))
print("-"*50)
