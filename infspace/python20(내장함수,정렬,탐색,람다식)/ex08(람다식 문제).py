# 람다식을 이용한 문제

# 1. 옷가게에서 주문을 받았다고 가정한다.
#   -----------------------------------
#   주문번호   물품   수량   품목당 가격
#   1.        "재킷"   5     120,000원
#   2.        "셔츠"   6     24,000원
#   3.        "바지"   3     50,000원
#   4.        "코트"   6     300,000원
#   -----------------------------------

# 위 주문내용을 리스트로 만들어서 주문당 주문번호 및
# 총 주문가격(수량 * 품목당 가격)을 람다식으로 만들어서
# 튜플리스트로 출력하는 프로그램을 작성하시오.
# 출력결과
# [("1", 600000),("2",144000),("3",150000),("4",1800000)]

# 풀이
# 위의 데이터를 더블리스트로 작성함.
orders = [["1","재킷",5,120000],
          ["2","셔츠",6,24000],
          ["3","바지",3,50000],
          ["4","코트",6,30000]]
# 람다식을 이용한 풀이
result = list(map(lambda x: (x[0],x[2]*x[3]), orders))
print(result)
print("-"*50)
