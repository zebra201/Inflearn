# 내장 함수 및 math 라이브러리 함수 이용
# 모듈은 import, 라이브러리는 from _ import *
# math 라이브러리의 모든 함수를 사용할 수 있게끔 불러옴

from math import *


# 절대값을 구하기
print(abs(-77))
print(abs(77))

# 반올림 하기
print(round(1.222))
print(round(1.522))

# 주어진 매개변수 값 중에서 최대값 구하기
print(max(10,-20,100,9999))

# 주어진 매개변수 값 중에서 최솟값 구하기
print(min(10,-20,100,9999))

# 4.0 의 제곱근 구하기
print(sqrt(4.0))

# 3의 2승 구하기
print(pow(3,2))

